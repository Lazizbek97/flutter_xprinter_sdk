  0x0410: 0x80, // А
  0x0411: 0x81, // Б
  0x0412: 0x82, // В
  0x0413: 0x83, // Г
  0x0414: 0x84, // Д
  0x0415: 0x85, // Е
  0x0416: 0x86, // Ж
  0x0417: 0x87, // З
  0x0418: 0x88, // И
  0x0419: 0x89, // Й
  0x041A: 0x8A, // К
  0x041B: 0x8B, // Л
  0x041C: 0x8C, // М
  0x041D: 0x8D, // Н
  0x041E: 0x8E, // О
  0x041F: 0x8F, // П
  0x0420: 0x90, // Р
  0x0421: 0x91, // С
  0x0422: 0x92, // Т
  0x0423: 0x93, // У
  0x0424: 0x94, // Ф
  0x0425: 0x95, // Х
  0x0426: 0x96, // Ц
  0x0427: 0x97, // Ч
  0x0428: 0x98, // Ш
  0x0429: 0x99, // Щ
  0x042A: 0x9A, // Ъ
  0x042B: 0x9B, // Ы
  0x042C: 0x9C, // Ь
  0x042D: 0x9D, // Э
  0x042E: 0x9E, // Ю
  0x042F: 0x9F, // Я
  // ── Lowercase first half (а–п) ─────────────────────────────────────────────
  0x0430: 0xA0, // а
  0x0431: 0xA1, // б
  0x0432: 0xA2, // в
  0x0433: 0xA3, // г
  0x0434: 0xA4, // д
