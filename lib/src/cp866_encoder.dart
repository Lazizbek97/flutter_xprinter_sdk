  0x043F: 0xAF, // п
  // ── Lowercase second half (р–я) ────────────────────────────────────────────
  0x0440: 0xE0, // р
  0x0441: 0xE1, // с
  0x0442: 0xE2, // т
  0x0443: 0xE3, // у
